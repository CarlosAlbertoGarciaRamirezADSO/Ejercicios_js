// Escribir un programa que calcule la velocidad de un proyectil que recorre 2km en 5
// minutos, exprese el resultado en metros / segundo.

let kilometro = 2;
let minutos = 5;

let metros = kilometro * 1000;
let segundo = minutos * 60;

let metros_segundos = metros / segundo;

alert(`La velocidad en metros/ segundos es: ${metros_segundos}`)


